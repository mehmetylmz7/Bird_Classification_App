def copy_dir_with_progress(src, dst, description):
    """
    KlasÃ¶rÃ¼ ilerleme Ã§ubuÄŸu ile kopyalar.
    """
    # 1. Ã–nce kopyalanacak toplam dosya sayÄ±sÄ±nÄ± hesapla
    print(f"ğŸ“Š '{description}' iÃ§in dosya sayÄ±sÄ± hesaplanÄ±yor... (Biraz bekleyin)")
    total_files = 0
    for root, dirs, files in os.walk(src):
        total_files += len(files)

    print(f"âœ… Toplam {total_files} dosya bulundu. Kopyalama baÅŸlÄ±yor...")

    # 2. Kopyalama iÅŸlemi
    if not os.path.exists(dst):
        os.makedirs(dst)

    # Ä°lerleme Ã§ubuÄŸunu oluÅŸtur
    with tqdm(total=total_files, desc=description, unit='img') as pbar:
        for root, dirs, files in os.walk(src):
            # Hedef klasÃ¶r yapÄ±sÄ±nÄ± oluÅŸtur
            rel_path = os.path.relpath(root, src)
            dest_dir = os.path.join(dst, rel_path)

            if not os.path.exists(dest_dir):
                os.makedirs(dest_dir)

            # DosyalarÄ± tek tek kopyala ve Ã§ubuÄŸu gÃ¼ncelle
            for file in files:
                src_file = os.path.join(root, file)
                dst_file = os.path.join(dest_dir, file)

                shutil.copy2(src_file, dst_file) # DosyayÄ± kopyala
                pbar.update(1) # Ã‡ubuÄŸu 1 ilerlet

