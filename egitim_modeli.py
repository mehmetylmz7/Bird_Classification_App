import time
import copy
import torch.optim as optim

# --- 1. EÄÄ°TÄ°M FONKSÄ°YONU ---
def train_model(model, criterion, optimizer, num_epochs=10):
    since = time.time()

    # En iyi modelin aÄŸÄ±rlÄ±klarÄ±nÄ± saklamak iÃ§in
    best_model_wts = copy.deepcopy(model.state_dict())
    best_acc = 0.0

    # Ä°statistikleri tutacaÄŸÄ±mÄ±z liste
    history = {'train_loss': [], 'train_acc': [], 'test_loss': [], 'test_acc': []}

    for epoch in range(num_epochs):
        print(f'Epoch {epoch+1}/{num_epochs}')
        print('-' * 10)

        # Her epoch'ta Training ve Test fazÄ±
        for phase in ['train', 'test']:
            if phase == 'train':
                model.train()  # EÄŸitim modu
                dataloader = train_loader
            else:
                model.eval()   # DeÄŸerlendirme modu
                dataloader = test_loader

            running_loss = 0.0
            running_corrects = 0

            # Veriler Ã¼zerinde dÃ¶ngÃ¼
            for inputs, labels in dataloader:
                inputs = inputs.to(device)
                labels = labels.to(device)

                optimizer.zero_grad()

                # Forward (Ä°leri YayÄ±lÄ±m)
                with torch.set_grad_enabled(phase == 'train'):
                    outputs = model(inputs)
                    _, preds = torch.max(outputs, 1)
                    loss = criterion(outputs, labels)

                    # Backward (Geri YayÄ±lÄ±m) - Sadece eÄŸitimde
                    if phase == 'train':
                        loss.backward()
                        optimizer.step()

                # Ä°statistikler
                running_loss += loss.item() * inputs.size(0)
                running_corrects += torch.sum(preds == labels.data)

            epoch_loss = running_loss / dataset_sizes[phase]
            epoch_acc = running_corrects.double() / dataset_sizes[phase]

            print(f'{phase} Loss: {epoch_loss:.4f} Acc: {epoch_acc:.4f}')

            # KayÄ±t et
            if phase == 'train':
                history['train_acc'].append(epoch_acc.item())
                history['train_loss'].append(epoch_loss)
            else:
                history['test_acc'].append(epoch_acc.item())
                history['test_loss'].append(epoch_loss)

            # En iyi modeli kopyala
            if phase == 'test' and epoch_acc > best_acc:
                best_acc = epoch_acc
                best_model_wts = copy.deepcopy(model.state_dict())

        print() # BoÅŸ satÄ±r

    time_elapsed = time.time() - since
    print(f'EÄŸitim tamamlandÄ±: {time_elapsed // 60:.0f}dk {time_elapsed % 60:.0f}sn')
    print(f'En YÃ¼ksek Test BaÅŸarÄ±sÄ± (Acc): {best_acc:.4f}')

    # En iyi aÄŸÄ±rlÄ±klarÄ± yÃ¼kle
    model.load_state_dict(best_model_wts)
    return model, history

# --- 2. EÄÄ°TÄ°MÄ° BAÅLAT (ISINMA TURU - 3 EPOCH) ---
